TEST(Unsigned_Modulo, Basic) {
    Aeu128 one = 1, zero = 0, ten = 10, two = 2;
    EXPECT_EQ(zero % one, zero);
    EXPECT_EQ(ten % two, 0);
    EXPECT_EQ(two % ten, 2);
    EXPECT_EQ(ten % one, 0);
    EXPECT_EQ(one % ten, 1);
}

